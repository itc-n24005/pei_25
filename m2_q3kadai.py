# a と b のリストを代入
a = [2.02, 2.03, 2.04]
b = [2.05, 2.06, -2.07]

# c に結合
c = a + b

# c の最大値
print(max(c))

# c[2:5] の最小値
print(min(c[2:5]))

# 指定要素を少数第1位に丸めて出力
print(round(c[2], 1))
print(round(c[5], 1))
print(round(c[3], 1))  # → 2.0 になる理由は下に説明

# 2.05 は2進数で正確に表現できないため内部では 2.04999999... のような値になり、
# round(2.05, 1) は 2.0 に丸められる

