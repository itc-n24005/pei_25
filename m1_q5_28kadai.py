# 曜日のリストを作成
day = ['月','月','火','水','木','金','金']

# removeメソッドで重複している '月' を1つ削除
day.remove('月')  # リストの中から最初に見つかった '月' を削除する

# removeメソッドで重複している '金' を1つ削除
day.remove('金')  # リストの中から最初に見つかった '金' を削除する

# insertメソッドで '土' を追加
day.insert(5, '土')  # インデックス5の位置に '土' を挿入する

# insertメソッドで '日' を追加
day.insert(6, '日')  # インデックス6の位置に '日' を挿入する

# 最終的なリストを表示
print(day)

